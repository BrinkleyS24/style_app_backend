const mongoose = require('mongoose');

const ServiceProviderSchema = new mongoose.Schema({
  _id: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model('ServiceProvider', ServiceProviderSchema);
